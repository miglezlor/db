    private void BAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAddActionPerformed
        Metodos con = new Metodos();
        Connection re = con.conexion();
        String dorsal, nombre, apell, demarc;
        String sql;
        
        dorsal = TFieldDorsal.getText();
        nombre = TFieldNombre.getText();
        apell = TFieldApell.getText();
        demarc = TFieldDemar.getText();
        
        sql = "INSERT INTO celpla (Dorsal,Nombre,Apellido,Demarcacion) VALUES (?,?,?,?)";
        try {
            PreparedStatement prepsta =  re.prepareStatement(sql);
            prepsta.setString(1, dorsal);
            prepsta.setString(2, nombre);
            prepsta.setString(3, apell);
            prepsta.setString(4, demarc);
            int n = prepsta.executeUpdate();
            if(n>0){
                JOptionPane.showMessageDialog(this, "Insercion realizada");
            }
            else
                JOptionPane.showMessageDialog(this, "Error en la insercion");
        } catch (SQLException ex) {
            Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BAddActionPerformed

    private void BConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BConsultarActionPerformed
        ResultSet resul = null;
        Statement stat = null;
        String sql = JOptionPane.showInputDialog("Introduce la consulta");
        try {
            stat = (Statement) uhum.conexion().createStatement();
        } catch (SQLException ex) {
            Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            resul = stat.executeQuery(sql);
        } catch (SQLException ex) {
            Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String visualiz ="DORSAL NOMBRE  APELLIDO   DEMARCACION \n-----------------------------------------------------------------\n";
                
        try {
            while (resul.next()) {
                visualiz = visualiz +"  "+ resul.getBigDecimal("Dorsal")+"               "+resul.getString("Nombre")+"            "+resul.getString("Apellido")+"          "+resul.getString("Demarcacion")+"\n" ;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
        }
                JOptionPane.showMessageDialog(this, visualiz);
        
        
    }//GEN-LAST:event_BConsultarActionPerformed

